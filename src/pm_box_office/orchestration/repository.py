def refresh_table_metric(
    conn: Any,
    *,
    source_key: str,
    metric_key: str,
    table_name: str,
    timestamp_column: str,
) -> None:
    if not relation_exists(conn, table_name):
        upsert_freshness(conn, source_key=source_key, metric_key=metric_key, latest_collected_at=None, row_count=0)
        return
    row = conn.execute(
        f"""
        SELECT MAX({timestamp_column}::timestamptz), COUNT(*)::bigint
        FROM {table_name}
        WHERE {timestamp_column} IS NOT NULL
        """
    ).fetchone()
    upsert_freshness(
        conn,
        source_key=source_key,
        metric_key=metric_key,
        latest_collected_at=row[0] if row else None,
        row_count=int(row[1] if row else 0),
    )


def upsert_freshness(
    conn: Any,
    *,
    source_key: str,
    metric_key: str,
    latest_collected_at: dt.datetime | None,
    row_count: int,
    details: dict[str, Any] | None = None,
) -> None:
    conn.execute(
        """
        INSERT INTO source_freshness (
            source_key, metric_key, latest_collected_at, row_count, details, updated_at
        )
        VALUES (%s, %s, %s, %s, %s::jsonb, CURRENT_TIMESTAMP)
        ON CONFLICT(source_key, metric_key) DO UPDATE SET
            latest_collected_at = excluded.latest_collected_at,
            row_count = excluded.row_count,
            details = excluded.details,
            updated_at = excluded.updated_at
        """,
        (source_key, metric_key, latest_collected_at, row_count, json.dumps(details or {})),
    )


def list_freshness(conn: Any, source_key: str) -> list[dict[str, Any]]:
    rows = conn.execute(
        """
        SELECT metric_key, latest_collected_at, row_count, updated_at
        FROM source_freshness
        WHERE source_key = %s
        ORDER BY metric_key
        """,
        (source_key,),
    ).fetchall()
    return [
        {
            "metric_key": row[0],
            "latest_collected_at": row[1],
            "row_count": row[2],
            "updated_at": row[3],
        }
        for row in rows
    ]


def has_any_movies(conn: Any) -> bool:
    if not relation_exists(conn, "movies"):
        return False
    row = conn.execute("SELECT EXISTS (SELECT 1 FROM movies LIMIT 1)").fetchone()
    return bool(row and row[0])


def relation_exists(conn: Any, relation_name: str) -> bool:
    row = conn.execute("SELECT to_regclass(%s)", (relation_name,)).fetchone()
    return bool(row and row[0])


def as_string_list(value: Any) -> list[str]:
    if value is None:
        return []
    if isinstance(value, str):
        try:
            parsed = json.loads(value)
        except json.JSONDecodeError:
            return [value]
    else:
        parsed = value
    if isinstance(parsed, list):
        return [str(item) for item in parsed]
    return [str(parsed)]
