def list_movies_for_date(conn: Any, exhibition_date: dt.date) -> list[MovieInventoryRow]:
    rows = conn.execute(
        """
        SELECT
            s.amc_movie_id,
            MAX(s.amc_movie_name) AS amc_movie_name,
            COUNT(DISTINCT s.amc_theatre_id)::integer AS theatre_count,
            COUNT(*)::integer AS showtime_count,
            COALESCE(MAX(CASE WHEN cm.selected THEN 1 ELSE 0 END), 0)::integer AS selected
        FROM amc_showtimes s
        LEFT JOIN collection_campaigns c ON c.exhibition_date = s.exhibition_date
        LEFT JOIN campaign_movies cm ON cm.campaign_id = c.campaign_id AND cm.amc_movie_id = s.amc_movie_id
        WHERE s.exhibition_date = %s
        GROUP BY s.amc_movie_id
        ORDER BY showtime_count DESC, amc_movie_name
        """,
        (exhibition_date,),
    ).fetchall()
    return [
        MovieInventoryRow(
            amc_movie_id=str(row[0]),
            amc_movie_name=str(row[1]),
            theatre_count=int(row[2]),
            showtime_count=int(row[3]),
            selected=bool(row[4]),
        )
        for row in rows
    ]


def set_campaign_movie_selected(
    conn: Any,
    *,
    campaign_id: uuid.UUID,
    amc_movie_id: str,
    selected: bool,
) -> None:
    conn.execute(
        """
        INSERT INTO campaign_movies (
            campaign_id, amc_movie_id, selected, selected_at, deselected_at
        ) VALUES (%s, %s, %s, %s, %s)
        ON CONFLICT(campaign_id, amc_movie_id) DO UPDATE SET
            selected = excluded.selected,
            selected_at = CASE WHEN excluded.selected THEN excluded.selected_at ELSE campaign_movies.selected_at END,
            deselected_at = CASE WHEN excluded.selected THEN NULL ELSE excluded.deselected_at END
        """,
        (campaign_id, amc_movie_id, selected, utc_now() if selected else None, utc_now() if not selected else None),
    )


def set_campaign_movies_selected(
    conn: Any,
    *,
    campaign_id: uuid.UUID,
    amc_movie_ids: Iterable[str],
    selected: bool,
) -> int:
    movie_ids = list(dict.fromkeys(amc_movie_ids))
    conn.executemany(
        """
        INSERT INTO campaign_movies (
            campaign_id, amc_movie_id, selected, selected_at, deselected_at
        ) VALUES (%s, %s, %s, %s, %s)
        ON CONFLICT(campaign_id, amc_movie_id) DO UPDATE SET
            selected = excluded.selected,
            selected_at = CASE WHEN excluded.selected THEN excluded.selected_at ELSE campaign_movies.selected_at END,
            deselected_at = CASE WHEN excluded.selected THEN NULL ELSE excluded.deselected_at END
        """,
        [
            (
                campaign_id,
                movie_id,
                selected,
                utc_now() if selected else None,
                utc_now() if not selected else None,
            )
            for movie_id in movie_ids
        ],
    )
    return len(movie_ids)


def cancel_future_seat_tasks_for_movie(conn: Any, *, campaign_id: uuid.UUID, amc_movie_id: str) -> int:
    rows = conn.execute(
        """
        UPDATE collection_tasks t
        SET status = 'cancelled', completed_at = %s
        FROM collection_runs r
        WHERE t.run_id = r.run_id
          AND r.campaign_id = %s
          AND t.amc_movie_id = %s
          AND t.task_type = 'collect_seat_snapshot'
          AND t.status IN ('queued', 'retry')
          AND t.scheduled_for > CURRENT_TIMESTAMP
        RETURNING t.run_id
        """,
        (utc_now(), campaign_id, amc_movie_id),
    ).fetchall()
    run_ids = {as_uuid(row[0]) for row in rows}
    for run_id in run_ids:
        refresh_run_counters(conn, run_id)
    return len(rows)


