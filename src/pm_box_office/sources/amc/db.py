def stored_showtime_from_row(row: Any) -> StoredShowtime:
    timezone = str(row[6])
    local_start = row_datetime(row[4])
    if local_start.tzinfo is None:
        local_start = local_start.replace(tzinfo=ZoneInfo(timezone))
    return StoredShowtime(
        showtime_id=str(row[0]),
        amc_theatre_id=int(row[1]),
        theatre_slug=str(row[2]),
        local_show_date=row_date_text(row[3]),
        local_start_at=local_start,
        utc_start_at=ensure_utc(row_datetime(row[5])),
        timezone=timezone,
        amc_movie_id=str(row[7]),
        amc_movie_name=str(row[8]),
    )


